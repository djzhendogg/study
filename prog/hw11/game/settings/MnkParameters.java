package game.settings;

public class MnkParameters {
    private final int m;
    private final int n;
    private final int k;

    public MnkParameters(int m, int n, int k) {
        this.m = m;
        this.n = n;
        this.k = k;
    }

    public int getM() {
        return m;
    }

    public int getN() {
        return n;
    }

    public int getK() {
        return k;
    }
}
